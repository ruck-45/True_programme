    // Nullish coalescing operator “ ?? ” 

        // it is used to give default values to variables if their values are set to “ null ” or they have not been initialized and have data-type : “ undefined ”

        // Syntax :
            // let user;
            // alert(user??"guest user");  //since “user” is undefined, so alert will show “guest user”.


